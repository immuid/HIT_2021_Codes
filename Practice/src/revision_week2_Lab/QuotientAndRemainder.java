package revision_week2_Lab;

public class QuotientAndRemainder {
	public static void main(String[] args) {
		int divident=25 , divider=4;
		System.out.println("The below process is a prining of Quotient and Remainder : \n");
		double Quotient = divident/divider;
		System.out.println("The Quotient of "+divident+" and "+divider+" is :" +Quotient);
		double Remainder= divident % divider;
		System.out.println("The Remainder of "+divident+" and "+divider+" is :" +Remainder);
	}

}
